package br.com.rafaelsoto.jxmind;

public class JxmindException extends Exception {


	private static final long serialVersionUID = 1L;

	public JxmindException() {
		super();

	}

	public JxmindException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public JxmindException(String arg0) {
		super(arg0);

	}

	public JxmindException(Throwable arg0) {
		super(arg0);

	}

	
	
}
